    client = gspread.authorize(creds)
    spreadsheet = client.open_by_url("https://docs.google.com/spreadsheets/d/1nKPVCZ6zAOfpqCjV6WfjkzCI55FA9r2yvi9XL3iIneo/edit")
    # ìˆœì„œê°€ ë°”ë€Œì–´ë„ IDë¡œ íƒ­ì„ ì°¾ìŒ
