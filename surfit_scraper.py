# [ê³µí†µ] ë¸Œë¼ìš°ì € ì‹¤í–‰
def get_driver():
    options = Options()
    options.add_argument("--headless")
    options.add_argument("--disable-blink-features=AutomationControlled")
    driver = webdriver.Chrome(options=options)
    driver.execute_cdp_cmd("Page.addScriptToEvaluateOnNewDocument", {"source": "Object.defineProperty(navigator, 'webdriver', {get: () => undefined})"})
    return driver

# [ì „ìš©] ë°ì´í„° ìˆ˜ì§‘
def scrape_projects():
    driver = get_driver()
    new_data = []
    today = datetime.now().strftime("%Y-%m-%d")
    try:
        driver.get(CONFIG["url"])
        time.sleep(5)
        for _ in range(3): # ìŠ¤í¬ë¡¤ 3íšŒ
            driver.execute_script("window.scrollTo(0, document.body.scrollHeight);")
            time.sleep(1)
        
        for art in driver.find_elements(By.CSS_SELECTOR, "a"):
            title = art.text.strip()
            link = art.get_attribute("href")
            if link and title and len(title) > 10 and "ë¡œê·¸ì¸" not in title:
                if not any(d['url'] == link for d in new_data):
                    new_data.append({'title': title, 'url': link, 'scraped_at': today})
    finally: driver.quit()
    return new_data

